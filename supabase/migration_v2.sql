    and o.status='delivered'
    and not exists(select 1 from public.store_reviews r where r.order_id=o.id)
  order by o.delivered_at desc;
$$;

create or replace function public.respond_to_review(p_review_id uuid, p_message text)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  if not public.has_role(array['owner','admin']) then raise exception 'Acesso negado'; end if;
  insert into public.review_responses(review_id,author_id,message)
  values(p_review_id,auth.uid(),p_message)
  on conflict (review_id) do update set author_id=auth.uid(),message=excluded.message,updated_at=now();
  perform public.log_admin_action('review_response','review',p_review_id::text,null,jsonb_build_object('message',p_message),null);
end;
$$;

create or replace function public.toggle_review_visibility(p_review_id uuid, p_reason text)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare v_old public.store_reviews%rowtype; v_new_status text;
begin
  if not public.has_role(array['owner','admin']) then raise exception 'Acesso negado'; end if;
  select * into v_old from public.store_reviews where id=p_review_id;
  v_new_status := case when v_old.status='published' then 'hidden' else 'published' end;
  update public.store_reviews set status=v_new_status,moderation_reason=p_reason,moderated_by=auth.uid(),moderated_at=now(),updated_at=now() where id=p_review_id;
  perform public.log_admin_action('review_moderation','review',p_review_id::text,to_jsonb(v_old),jsonb_build_object('status',v_new_status),p_reason);
end;
$$;

