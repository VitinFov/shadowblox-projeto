create or replace function public.set_user_role(p_user_id uuid, p_role text)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare v_old public.profiles%rowtype;
begin
  if public.current_role() <> 'owner' then raise exception 'Somente o proprietário pode alterar cargos'; end if;
  if p_role not in ('admin','delivery_staff','customer') then raise exception 'Cargo inválido'; end if;
  select * into v_old from public.profiles where id=p_user_id;
  if v_old.role='owner' then raise exception 'O proprietário não pode ser rebaixado'; end if;
  update public.profiles set role=p_role,is_admin=(p_role='admin'),updated_at=now() where id=p_user_id;
  perform public.log_admin_action('set_role','profile',p_user_id::text,to_jsonb(v_old),jsonb_build_object('role',p_role),null);
end;
$$;

create or replace function public.set_user_blocked(p_user_id uuid, p_blocked boolean, p_reason text default null)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare v_old public.profiles%rowtype;
begin
  if public.current_role() <> 'owner' then raise exception 'Somente o proprietário pode bloquear usuários'; end if;
  select * into v_old from public.profiles where id=p_user_id;
  if v_old.role='owner' then raise exception 'O proprietário não pode ser bloqueado'; end if;
  update public.profiles set blocked=p_blocked,blocked_reason=case when p_blocked then p_reason else null end,updated_at=now() where id=p_user_id;
  perform public.log_admin_action('set_blocked','profile',p_user_id::text,to_jsonb(v_old),jsonb_build_object('blocked',p_blocked),p_reason);
end;
$$;

