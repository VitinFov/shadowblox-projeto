create or replace function public.get_public_reviews()
returns table(
  id uuid,
  rating integer,
  comment text,
  status text,
  created_at timestamptz,
  display_name text,
  avatar_url text,
  response_message text
)
language sql
stable
security definer
set search_path = public
as $$
  select r.id,r.rating,r.comment,r.status,r.created_at,
         coalesce(p.full_name,'Cliente') as display_name,
         p.avatar_url,
         rr.message as response_message
  from public.store_reviews r
  join public.profiles p on p.id=r.user_id
  left join public.review_responses rr on rr.review_id=r.id
  where r.status='published'
  order by r.created_at desc;
$$;

