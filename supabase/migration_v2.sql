create or replace function public.create_order(p_payment_method text, p_items jsonb)
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  v_order_id uuid := gen_random_uuid();
  v_item jsonb;
  v_product public.products%rowtype;
  v_quantity integer;
  v_total integer := 0;
  v_profile public.profiles%rowtype;
begin
  if auth.uid() is null then raise exception 'Faça login para finalizar a compra'; end if;
  if p_payment_method not in ('pix','card') then raise exception 'Forma de pagamento inválida'; end if;
  if jsonb_array_length(p_items) = 0 then raise exception 'Carrinho vazio'; end if;

  select * into v_profile from public.profiles where id = auth.uid();
  if coalesce(v_profile.blocked,false) then raise exception 'Conta bloqueada'; end if;

  insert into public.orders(id,user_id,customer_name,customer_email,status,payment_method,total_cents)
  values(v_order_id,auth.uid(),v_profile.full_name,v_profile.email,'awaiting_payment',p_payment_method,0);

  for v_item in select * from jsonb_array_elements(p_items)
  loop
    v_quantity := greatest(1, least(100, coalesce((v_item->>'quantity')::integer,1)));
    select * into v_product from public.products where id = (v_item->>'product_id')::uuid for share;
    if not found then raise exception 'Produto não encontrado'; end if;
    if not v_product.active or not v_product.visible or v_product.out_of_stock or v_product.stock < v_quantity then
      raise exception 'Produto indisponível: %', v_product.name;
    end if;
    v_total := v_total + (v_product.price_cents * v_quantity);
    insert into public.order_items(order_id,product_id,product_name,quantity,unit_price_cents)
    values(v_order_id,v_product.id,v_product.name,v_quantity,v_product.price_cents);
  end loop;

  update public.orders set total_cents = v_total, updated_at = now() where id = v_order_id;
  return v_order_id;
exception when others then
  delete from public.orders where id = v_order_id;
  raise;
end;
$$;

create or replace function public.submit_payment_receipt(p_order_id uuid, p_file_path text)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  if not exists(select 1 from public.orders where id=p_order_id and user_id=auth.uid()) then
    raise exception 'Pedido não encontrado';
  end if;
  insert into public.payment_receipts(order_id,user_id,file_path)
  values(p_order_id,auth.uid(),p_file_path)
  on conflict do nothing;
  update public.orders set status='under_review',updated_at=now() where id=p_order_id and user_id=auth.uid();
end;
$$;

create or replace function public.set_order_status(p_order_id uuid, p_status text)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare v_old public.orders%rowtype;
begin
  if not public.has_role(array['owner','admin']) then raise exception 'Acesso negado'; end if;
  if p_status not in ('under_review','paid','rejected','cancelled','refunded') then raise exception 'Status inválido'; end if;
  select * into v_old from public.orders where id=p_order_id;
  update public.orders set status=p_status,updated_at=now() where id=p_order_id;
  perform public.log_admin_action('order_status','order',p_order_id::text,to_jsonb(v_old),jsonb_build_object('status',p_status),null);
  if p_status='paid' and not exists(select 1 from public.chat_messages where order_id=p_order_id) then
    insert into public.chat_messages(order_id,sender_id,message)
    values(p_order_id,auth.uid(),'Pagamento aprovado! Mande uma mensagem para receber o item.');
  end if;
end;
$$;

create or replace function public.claim_order(p_order_id uuid)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare v_order public.orders%rowtype;
begin
  if not public.has_role(array['owner','admin','delivery_staff']) then raise exception 'Acesso negado'; end if;
  select * into v_order from public.orders where id=p_order_id for update;
  if v_order.status not in ('paid','in_service') then raise exception 'Pedido não está disponível para atendimento'; end if;
  if v_order.assigned_to is not null and v_order.assigned_to <> auth.uid() and public.current_role()='delivery_staff' then
    raise exception 'Este pedido já foi assumido por outro funcionário';
  end if;
  update public.orders set assigned_to=coalesce(assigned_to,auth.uid()),assigned_at=coalesce(assigned_at,now()),status='in_service',updated_at=now() where id=p_order_id;
  perform public.log_admin_action('claim_order','order',p_order_id::text,to_jsonb(v_order),jsonb_build_object('assigned_to',auth.uid(),'status','in_service'),null);
end;
$$;

create or replace function public.mark_order_delivered(p_order_id uuid)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare v_order public.orders%rowtype;
begin
  select * into v_order from public.orders where id=p_order_id for update;
  if not (
    public.has_role(array['owner','admin'])
    or (public.current_role()='delivery_staff' and v_order.assigned_to=auth.uid())
  ) then raise exception 'Acesso negado'; end if;
  if v_order.status not in ('paid','in_service') then raise exception 'Pedido não pode ser entregue neste status'; end if;
  update public.orders set status='delivered',delivered_at=now(),updated_at=now() where id=p_order_id;
  perform public.log_admin_action('deliver_order','order',p_order_id::text,to_jsonb(v_order),jsonb_build_object('status','delivered'),null);
end;
$$;

